const urlLang = new URLSearchParams(window.location.search).get('lang');
const savedLang = localStorage.getItem('lang');
let currentLang = urlLang && T[urlLang] ? urlLang : (savedLang && T[savedLang] ? savedLang : 'en');

function t(key) {
  return (T[currentLang] && T[currentLang][key]) || (T['en'] && T['en'][key]) || key;
}

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('lang', lang);
  document.documentElement.lang = lang;
  // Update all translatable elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = t(key);
    } else {
      el.textContent = t(key);
    }
  });
  // Update lang switcher display
  document.querySelectorAll('.lang-option').forEach(el => {
    el.classList.toggle('active', el.dataset.lang === lang);
  });
  // Update home page title only; product pages keep their product-specific SEO titles.
  if (!window.location.pathname.includes('/products/')) {
    document.title = 'Qiayun Electric Power | ' + t('hero_title');
  }
}

function initI18n() {
  setLang(currentLang);
}
